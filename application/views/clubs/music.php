            <article  id="events-music" class="tab-pane fade">
<p>
    <h3>Freshers' Nite</h3>
The first event of the year, it is the time when the new first year members are inducted into the club. The freshers are prepared by the senior batches of the club to perform in the Freshers' Nite.</p>
<p><h3>Musical Extravaganza</h3>
'The Musical Extravaganza' is the bi-annual flagship event where a number of songs are covered and performed by the club. From jazzy to head-bangable numbers; the club covers a huge variety of genres for the same. All the songs are ear picked and the newer members are initiated into this method as well.</p>
<p><h3>Acoustic Night</h3>
This event only uses acoustic instruments like the acoustic guitar, piano (on the synth), bass, drums, and perhaps flute/violin etc in addition to vocals. Nothing synthesized, to a large extent. The club also allows solo performers or non-member bands to perform in the event post the auditions.</p>
<p><h3>Other Events</h3>
The club also performs in events like Founder's Day celebrations, Independence Day and Republic Day celebrations, Freshers' Orientation, etc.
</p>
            </article>

            <article id="joinus-music" class="tab-pane fade">
<p>
